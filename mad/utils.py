def calc_memory_gb(data_specs, num_steps):
    ''' Calculates memory necessary for replay buffer and allocates it once to check. Slows program startup'''
    def get_size(spec):
        if isinstance(spec, dict):
            return sum(get_size(s) for s in spec.values())
        return np.prod(spec.shape) * np.dtype(spec.dtype).itemsize
    
    total_bytes = sum(get_size(spec) for spec in data_specs) * num_steps
    gb_needed = total_bytes / (1024**3)
    gb_needed = round(gb_needed, 2)
    print("Checking memory requirement: ", gb_needed, "GB")
    try:
        arr = np.zeros(int((gb_needed) * 1024 * 1024 * 1024 / 4), dtype=np.float32)
        del arr
    except:
        print("Not enough cpu memory for replay buffer, tried to allocate: ", gb_needed," GB. Try reducing frame_stack in config to 1.")
        exit()
    return

